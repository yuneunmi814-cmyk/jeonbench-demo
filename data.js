// ── 동종 분포 생성(결정론적) ──
function genDist(median, cv, n) {
  const arr = [];
  for (let i = 0; i < n; i++) {
    const q = (i + 0.5) / n;
    const z = Math.sqrt(2) * inverf(2 * q - 1);
    arr.push(Math.max(median * (1 + cv * z), median * 0.45));
  }
  return arr.sort((a, b) => a - b);
}
function inverf(x) {
  const a = 0.147, ln = Math.log(1 - x * x);
  const t = 2 / (Math.PI * a) + ln / 2;
  return Math.sign(x) * Math.sqrt(Math.sqrt(t * t - ln / a) - t);
}

